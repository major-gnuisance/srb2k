tic_t lerp_currenttic = 0; fixed_t lerp_fractic; boolean lerp_sameframe;
tic_t rendergametic;

void D_SRB2Loop(void)
{
	tic_t oldentertics = 0, entertic = 0, realtics = 0, rendertimeout = INFTICS;
	// init frame time var
	rs_prevframetime = I_GetTimeMicros();

	if (dedicated)
		server = true;

	// Pushing of + parameters is now done back in D_SRB2Main, not here.

	CONS_Printf("I_StartupKeyboard()...\n");
	I_StartupKeyboard();

#ifdef _WINDOWS
	CONS_Printf("I_StartupMouse()...\n");
	I_DoStartupMouse();
#endif

	oldentertics = I_GetTime();

	// end of loading screen: CONS_Printf() will no more call FinishUpdate()
	con_startup = false;

	// make sure to do a d_display to init mode _before_ load a level
	SCR_SetMode(); // change video mode
	SCR_Recalc();

	// Check and print which version is executed.
	// Use this as the border between setup and the main game loop being entered.
	CONS_Printf(
	"===========================================================================\n"
	"                   We hope you enjoy this game as\n"
	"                     much as we did making it!\n"
	"===========================================================================\n");

	// hack to start on a nice clear console screen.
	COM_ImmedExecute("cls;version");

	if (rendermode == render_soft)
		V_DrawFixedPatch(0, 0, FRACUNIT/2, 0, (patch_t *)W_CacheLumpNum(W_GetNumForName("KARTKREW"), PU_CACHE), NULL);
	I_FinishUpdate(); // page flip or blit buffer

	for (;;)
	{
		if (lastwipetic)
		{
			oldentertics = lastwipetic;
			lastwipetic = 0;
		}

		// get real tics
		entertic = I_GetTime();
		realtics = entertic - oldentertics;
		oldentertics = entertic;

		refreshdirmenu = 0; // not sure where to put this, here as good as any?

#ifdef DEBUGFILE
		if (!realtics)
			if (debugload)
				debugload--;
#endif

		if (demo.playback && gamestate == GS_LEVEL)
		{
			//static fixed_t oldlerp = 0;
			//fixed_t lerp = I_GetFracTime();
			realtics = realtics * cv_playbackspeed.value;// + FixedMul(lerp, cv_playbackspeed.value) - FixedMul(oldlerp, cv_playbackspeed.value);
			//oldlerp = lerp;
		}

		if (!realtics && !singletics)// ?? if no tic progression has happened
		{
			//I_Sleep();//test
			if (cv_interpolationmode.value != 0 && gamestate == GS_LEVEL)
			{
				if (rendertimeout == entertic+TICRATE/17)
				{
					fixed_t old = lerp_fractic;
					int i = 0;
					h_debug_var = true;

					if (demo.playback && gamestate == GS_LEVEL)
						lerp_fractic = (I_GetFracTime() * cv_playbackspeed.value) % FRACUNIT - cv_extrapolation.value;
					else
						lerp_fractic = I_GetFracTime() - cv_extrapolation.value;

					//if (lerp_fractic < old)
						//CONS_Printf("lerp_fractic < old\n");
					while (lerp_fractic < old)
					{
						i++;
						//CONS_Printf("Increment lerp_fractic by fu, i %d\n", i);
						lerp_fractic += FRACUNIT;
						//break;// test: always only do once
					}
				}
				else
				{
					h_debug_var = false;
				}

				if (D_Display())
				{
					lerp_sameframe = true;

					if (moviemode)
						M_SaveFrame();
					if (takescreenshot) // Only take screenshots after drawing.
						M_DoScreenShot();
				}
			}
			else
				I_Sleep();// only sleep in 35 fps mode. Frame rate cap has a separate sleep

			continue;
		}

		lerp_sameframe = false;

#ifdef HW3SOUND
		HW3S_BeginFrameUpdate();
#endif

		// don't skip more than 10 frames at a time
		// (fadein / fadeout cause massive frame skip!)
		if (realtics > 8)
			realtics = 1;

		// process tics (but maybe not if realtic == 0)
		TryRunTics(realtics);

		if (lastdraw || singletics || gametic > rendergametic)
		{
			rendergametic = gametic;
			rendertimeout = entertic+TICRATE/17;

			// Update display, next frame, with current state.
			if (cv_interpolationmode.value == 0)
				lerp_fractic = 0;
			else if (demo.playback && gamestate == GS_LEVEL)
				lerp_fractic = (I_GetFracTime() * cv_playbackspeed.value) % FRACUNIT - cv_extrapolation.value;
			else
				lerp_fractic = I_GetFracTime() - cv_extrapolation.value;
			if (D_Display())
			{
				lerp_sameframe = true;

				if (moviemode)
					M_SaveFrame();
				if (takescreenshot) // Only take screenshots after drawing.
					M_DoScreenShot();
			}
		}
		else if (rendertimeout < entertic) // in case the server hang or netsplit
		{
			lerp_fractic = FRACUNIT - cv_extrapolation.value;

			if (D_Display())
			{
				lerp_sameframe = true;

				//CONS_Printf("Hang part got run!\n");

				if (moviemode)
					M_SaveFrame();
				if (takescreenshot) // Only take screenshots after drawing.
					M_DoScreenShot();
			}
		}

		// consoleplayer -> displayplayers (hear sounds from viewpoint)
		S_UpdateSounds(); // move positional sounds

		// check for media change, loop music..
		I_UpdateCD();

#ifdef HW3SOUND
		HW3S_EndFrameUpdate();
#endif

#ifdef HAVE_BLUA
		LUA_Step();
#endif
	}
}

